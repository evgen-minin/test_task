#!/usr/bin/python3


def sum_of_digits(number: int) -> int:
    """
    Возвращает сумму цифр числа.

    Аргументы:
    number -- целое число, для которого нужно найти сумму его цифр.

    Возвращает:
    Сумму цифр числа.
    """
    # Преобразуем число в строку, используем abs(), чтобы игнорировать знак числа
    # Пробегаемся по каждой цифре строки, преобразуем её обратно в int и суммируем все цифры
    return sum(int(digit) for digit in str(abs(number)))

def find_number_with_max_digit_sum() -> None:
    """
    Находит и выводит число с максимальной суммой цифр среди введённых пользователем чисел.
    Программа продолжает принимать числа от пользователя до тех пор, пока не введён ноль.
    """
    # Переменные для хранения максимальной суммы цифр и числа, которое имеет эту сумму
    max_sum: int = -1  # Начальное значение для максимальной суммы (минимально возможное)
    number_with_max_sum: int = 0  # Число с максимальной суммой цифр
    
    # Бесконечный цикл для ввода чисел
    while True:
        try:
            # Ввод числа от пользователя и его преобразование в целое число
            num: int = int(input("Введите целое число (0 для завершения): "))
        except ValueError:
            # Если пользователь ввел не целое число, выводим сообщение об ошибке и продолжаем цикл
            print("Некорректный ввод! Пожалуйста, введите целое число.")
            continue  # Возвращаемся в начало цикла

        # Прерываем цикл, если пользователь ввёл 0
        if num == 0:
            break

        # Вычисляем сумму цифр текущего числа
        digit_sum: int = sum_of_digits(num)
        
        # Если сумма цифр текущего числа больше, чем текущий максимум, обновляем максимум и сохраняем число
        if digit_sum > max_sum:
            max_sum = digit_sum
            number_with_max_sum = num
    
    # Когда цикл завершён, выводим число с максимальной суммой цифр
    print(f"Число с максимальной суммой цифр: {number_with_max_sum}")

# Запуск программы
if __name__ == "__main__":
    find_number_with_max_digit_sum()
